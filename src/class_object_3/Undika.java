package class_object_3;
public class Undika {
    private String nama, alamat;
    
    //setter
    public void setNama(String n){
        nama = n;
    }
    
    public void setAlamat(String a){
        alamat = a;
    }
    
    //getter
    public String getNama(){
        return this.nama;
    }
    
    public String getAlamat(){
        return this.alamat;
    }
    
}
